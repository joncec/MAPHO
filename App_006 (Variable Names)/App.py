# Python is case sensitive.
# Example:

A = 10

a = 99

# Variable names should be meaningful and self-explanatory, usually "snake_case" is used.
# Example:

average_car = "car"

starting_index = "something"

# Variable names can't:
# > Start with a number.
# > Include space.
# > Include special characters (@,#,$,%,+,-,=,*...).
# > Include keywords (and, for, return, True, if...)

# Obs: Try not to use Unicode Characters in variable names.